class AssignedReviewer():

    def __init__(self, reviewer_id, reviewer_code, grade, firstname, lastname, username, reviewer_code_id):
            self.reviewer_id = reviewer_id
            self.reviewer_code = reviewer_code
            self.grade = grade
            self.firstname = firstname
            self.lastname = lastname
            self.username = username
            self.reviewer_code_id = reviewer_code_id